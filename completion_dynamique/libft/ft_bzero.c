#include "./includes/libft.h"

void				ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	char			*end;

	i = 0;
	end = (char *)s;
	while (i < n)
	{
		end[i] = '\0';
		i++;
	}
	s = (void *)end;
}
